-- Formats a url for a school.
url :: String -> String -> Maybe Int -> String
url school page id = case lookup school schools' of
    Nothing -> error $ "School not found: " ++ school
    Just p  -> case id of
      Nothing -> "http://" ++ school ++ "/" ++ page ++ ".aspx?path=" ++ p
      Just id -> "http://" ++ school ++ "/" ++ page ++ ".aspx?path=" ++ p ++ "&" ++ page ++ "=" ++ show id

